    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            // Validasi input data
            String nama = FieldNama.getText();
            String beratjenis = FieldBeratjenis.getText();
            String presentase = TextPresentase.getText();
            String jenismaterial = ComboMaterial.getSelectedItem().toString();

            if (nama.isEmpty() || beratjenis.isEmpty() || presentase.isEmpty() || jenismaterial.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Semua field harus diisi", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Pastikan na adalah angka jika tipe data di database adalah integer
            try {
                Integer.parseInt(nama);
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Nama harus sesuai", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Pastikan nilai berat jenis tidak melebihi batas yang diperbolehkan
            if (beratjenis()>15) {
                JOptionPane.showMessageDialog(this, "Berat jenis", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            Connection conn = Config.getConnection();
            if (conn == null) {
                JOptionPane.showMessageDialog(this, "Failed to connect to the database.", "Database Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            PreparedStatement stmt = conn.prepareStatement("INSERT INTO Bahanbanguanabeton(nama, beratjenis, presentase, jenismaterial) VALUES (?, ?, ?, ?, ?)");
            stmt.setString(1, nama);
            stmt.setString(2, beratjenis);
            stmt.setString(3, presentase);
            stmt.setString(4, jenismaterial);

            stmt.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan", "Pesan", JOptionPane.INFORMATION_MESSAGE);
            TampilData();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
            System.out.println("SQL Exception: " + e.getMessage());
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

