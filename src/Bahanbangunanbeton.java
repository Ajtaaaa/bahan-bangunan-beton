    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
try {
            Connection conn = Config.getConnection();
            if (conn == null) {
                JOptionPane.showMessageDialog(this, "Failed to connect to the database.", "Database Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            String nama = FieldNama.getText();
            String beratjenis = FieldBeratjenis.getText();
            String presentase = FieldPresentase.getText();
            String jenismaterial = ComboMaterial.getSelectedItem().toString();

            // Validasi input data
            if (nama.isEmpty() || beratjenis.isEmpty() || presentase.isEmpty() || jenismaterial.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Semua field harus diisi", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Pastikan berat jenis adalah angka jika tipe data di database adalah integer
            try {
                Integer.parseInt(beratjenis);
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "beratjenis harus berupa angka", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Pastikan Presentase adalah angka jika tipe data di database adalah integer
            try {
                Integer.parseInt(presentase);
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Presentase harus berupa angka", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            PreparedStatement stmt = conn.prepareStatement(
                    "UPDATE Bahan_bangunan_beton SET nama = ?, beratjenis = ?, presentase = ?, jenismaterial = ?, WHERE nama = ?"
            );
            stmt.setString(1, nama);
            stmt.setString(2, beratjenis);
            stmt.setString(3, presentase);
            stmt.setString(4, jenismaterial);
            stmt.setString(6, nama); 
            // Set parameter keenam untuk where clause
            
            stmt.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil diubah", "Pesan", JOptionPane.INFORMATION_MESSAGE);
            TampilData();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
            System.out.println("SQL Exception: " + e.getMessage());
            e.printStackTrace();
        }                                  

    }//GEN-LAST:event_jButton3ActionPerformed

